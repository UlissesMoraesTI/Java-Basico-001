public class ComandStringToLowerCase {

    public static void main(String[] args) {
        String s = "Boa tarde!";
        s = s.toUpperCase();
        System.out.println(s); //Aplicado toUpperCase

        s = "BOM DIA!";
        s = s.toLowerCase();
        System.out.println(s); //Aplicado toLowerCase
    }
}
